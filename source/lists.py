# Create a list
fruits = ["apple", "banana", "cherry"]
print(fruits)

# Indexing
print(fruits[0])
print(fruits[-1])  # last element

# Append and extend
fruits.append("date")
print(fruits)

fruits.extend(["elderberry", "fig"])
print(fruits)

# Insert at position
fruits.insert(1, "apricot")
print(fruits)

# Length
print(len(fruits))
